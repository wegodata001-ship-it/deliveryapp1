/**
 * Embeds TTF fonts as base64 for client-side PDF export (jsPDF vfs).
 * Run: node scripts/embed-pdf-fonts.mjs
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const fontsDir = path.join(root, "public", "fonts");
const outFile = path.join(root, "src", "lib", "pdfFonts.ts");

const FONT_ENTRIES = [
  { vfsName: "Cairo-Hebrew.ttf", file: "IBMPlexSansHebrew-Regular.ttf" },
  { vfsName: "Cairo-Arabic.ttf", file: "NotoSansArabic-Regular.ttf" },
];

const vfs = {};
for (const { vfsName, file } of FONT_ENTRIES) {
  const filePath = path.join(fontsDir, file);
  if (!fs.existsSync(filePath)) {
    console.error(`Missing font: ${filePath}`);
    process.exit(1);
  }
  const buf = fs.readFileSync(filePath);
  if (buf.length < 10_000) {
    console.warn(`Warning: ${file} is only ${buf.length} bytes — may be corrupt.`);
  }
  vfs[vfsName] = buf.toString("base64");
  console.log(`Embedded ${file} → ${vfsName} (${buf.length} bytes)`);
}

const vfsJson = JSON.stringify(vfs, null, 0);

const ts = `/**
 * AUTO-GENERATED by scripts/embed-pdf-fonts.mjs — do not edit by hand.
 * Re-run after updating files in public/fonts/.
 */
export const LEDGER_PDF_FONT = "Cairo" as const;

export const LEDGER_PDF_VFS = ${vfsJson} as const;

export const LEDGER_PDF_FONT_FILES = {
  hebrew: "Cairo-Hebrew.ttf",
  arabic: "Cairo-Arabic.ttf",
} as const;

export function registerLedgerPdfFonts(doc: {
  addFileToVFS: (name: string, data: string) => void;
  addFont: (postScriptName: string, fontName: string, fontStyle: string) => void;
  setFont: (fontName: string, fontStyle?: string) => void;
}): void {
  for (const [name, b64] of Object.entries(LEDGER_PDF_VFS)) {
    doc.addFileToVFS(name, b64);
  }
  const { hebrew, arabic } = LEDGER_PDF_FONT_FILES;
  const family = LEDGER_PDF_FONT;
  doc.addFont(hebrew, family, "normal");
  doc.addFont(arabic, family, "italic");
  doc.addFont(hebrew, family, "bold");
  doc.addFont(arabic, family, "bolditalic");
  doc.setFont(family, "normal");
}

const ARABIC_SCRIPT_RE =
  /[\\u0600-\\u06FF\\u0750-\\u077F\\u08A0-\\u08FF\\uFB50-\\uFDFF\\uFE70-\\uFEFF]/u;

/** Pick Cairo style: Hebrew/Latin → normal, Arabic script → italic slot (Arabic TTF). */
export function ledgerPdfFontStyle(text: string): "normal" | "italic" {
  return ARABIC_SCRIPT_RE.test(text) ? "italic" : "normal";
}

export function setLedgerPdfFont(
  doc: { setFont: (fontName: string, fontStyle?: string) => void },
  text: string,
): void {
  doc.setFont(LEDGER_PDF_FONT, ledgerPdfFontStyle(text));
}
`;

fs.writeFileSync(outFile, ts, "utf8");
console.log(`Wrote ${outFile}`);

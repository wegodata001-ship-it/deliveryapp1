/**
 * Embeds TTF fonts as base64 for client-side PDF export (pdfmake vfs).
 * Run: node scripts/embed-pdf-fonts.mjs
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const fontsDir = path.join(root, "public", "fonts");
const outFile = path.join(root, "src", "lib", "pdfFonts.ts");

const FONT_ENTRIES = [
  { vfsKey: "IBMPlexSansHebrew-Regular.ttf", file: "IBMPlexSansHebrew-Regular.ttf" },
  { vfsKey: "NotoSansArabic-Regular.ttf", file: "NotoSansArabic-Regular.ttf" },
];

const vfs = {};
for (const { vfsKey, file } of FONT_ENTRIES) {
  const filePath = path.join(fontsDir, file);
  if (!fs.existsSync(filePath)) {
    console.error(`Missing font: ${filePath}`);
    process.exit(1);
  }
  const buf = fs.readFileSync(filePath);
  if (buf.length < 10_000) {
    console.warn(`Warning: ${file} is only ${buf.length} bytes — may be corrupt.`);
  }
  vfs[vfsKey] = buf.toString("base64");
  console.log(`Embedded ${file} → ${vfsKey} (${buf.length} bytes)`);
}

const vfsJson = JSON.stringify(vfs, null, 0);

const ts = `/**
 * AUTO-GENERATED by scripts/embed-pdf-fonts.mjs — do not edit by hand.
 * Re-run after updating files in public/fonts/.
 */
export const LEDGER_PDF_VFS = ${vfsJson} as const;

export const LEDGER_PDF_FONT_DEFS = {
  LedgerHebrew: {
    normal: "IBMPlexSansHebrew-Regular.ttf",
    bold: "IBMPlexSansHebrew-Regular.ttf",
    italics: "IBMPlexSansHebrew-Regular.ttf",
    bolditalics: "IBMPlexSansHebrew-Regular.ttf",
  },
  LedgerArabic: {
    normal: "NotoSansArabic-Regular.ttf",
    bold: "NotoSansArabic-Regular.ttf",
    italics: "NotoSansArabic-Regular.ttf",
    bolditalics: "NotoSansArabic-Regular.ttf",
  },
} as const;

export const LEDGER_PDF_DEFAULT_FONT = "LedgerHebrew" as const;

const ARABIC_SCRIPT_RE =
  /[\\u0600-\\u06FF\\u0750-\\u077F\\u08A0-\\u08FF\\uFB50-\\uFDFF\\uFE70-\\uFEFF]/u;

export function ledgerPdfFontFamily(text: string): "LedgerHebrew" | "LedgerArabic" {
  return ARABIC_SCRIPT_RE.test(text) ? "LedgerArabic" : "LedgerHebrew";
}
`;

fs.writeFileSync(outFile, ts, "utf8");
console.log(`Wrote ${outFile}`);
